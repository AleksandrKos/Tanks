public class SquareBorder {

    private int width;
    private int height;
    private int leftBottomCornerX;
    private int leftBottomCornerY;

    public SquareBorder(int width, int height, int leftBottomCornerX, int leftBottomCornerY){
        this.width = width;
        this.height = height;
        this.leftBottomCornerX = leftBottomCornerX;
        this.leftBottomCornerY = leftBottomCornerY;
    }
}
